      document.getElementById('popupReview').style.display = 'none';
    }

    document.addEventListener("DOMContentLoaded", function () {
      const track = document.getElementById("review-track");
      if (track) {
        // Cek jika sudah ada duplikasi konten
        if (track.children.length > 0 && !track.hasAttribute('data-duplicated')) {
          track.setAttribute('data-duplicated', 'true');
          track.innerHTML += track.innerHTML;
        }

        // Animasi carousel
        let position = 0;
        const speed = 1; // Kecepatan scroll (px per frame)

        function animate() {
          position -= speed;
          if (position <= -track.scrollWidth / 2) {
            position = 0;
          }
          track.style.transform = `translateX(${position}px)`;
          requestAnimationFrame(animate);
        }

        animate();
      }
    });


